#include "loguploadcallback.h"

LogUploadCallback::LogUploadCallback()
{

}

size_t
LogUploadCallback::writeCallback(char* buf, size_t size, size_t nmemb, void* up)
{
	data = "";

	for (int c = 0; c<size*nmemb; c++)
	{
		data.push_back(buf[c]);
	}
	return size*nmemb;
}
